    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        String id = model.getValueAt(index, 0).toString();
        String status = model.getValueAt(index, 8).toString();
        String newStatus = "";
        if (status.equals("Active")) {
            newStatus = "Inactive";
        } else {
            newStatus = "Active";
        }
        int a = JOptionPane.showConfirmDialog(null,
                "Do you want to change status of ID " + id + " to " + newStatus + "?",
                "Confirm", javax.swing.JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            StaffDao.changeStatus(Integer.parseInt(id), newStatus);
            setVisible(false);
            new VerifyStaffs().setVisible(true);
        }
    }//GEN-LAST:event_jTable1MouseClicked

