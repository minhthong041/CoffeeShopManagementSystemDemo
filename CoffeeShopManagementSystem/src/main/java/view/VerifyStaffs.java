    public void getAllRecords(String query) {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        ArrayList<Staff> list = StaffDao.getAllRecords(query);
        Iterator<Staff> itr = list.iterator();
        while (itr.hasNext()) {
            Staff staff = itr.next();
            String middleName = (staff.getMiddleName() == null) ? "" : staff.getMiddleName();
            String fullName = staff.getLastName() + " " + middleName + " " + staff.getFirstName();
            dtm.addRow(new Object[]{
                staff.getStaffId(),
                staff.getFirstName(),
                middleName,
                staff.getLastName(),
                fullName,
                staff.getRoleId(),
                staff.getDateOfHiring(),
                staff.getPassword(),
                staff.getStatus()
            });
        }
    }

