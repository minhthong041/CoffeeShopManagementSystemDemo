    public void tableDetails() {
        String searchField = (String) cboSearchField.getSelectedItem();
        String searchText = txtSearch.getText();
        String sort = (String) cboSort.getSelectedItem();
        ArrayList<Customer> list = CustomerDao.getCustomers(searchField, searchText, sort);
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        for (Customer c : list) {
            String middleName = (c.getMiddleName() == null) ? "" : c.getMiddleName();
            String fullName = c.getLastName() + " " + middleName + " " + c.getFirstName();
            dtm.addRow(new Object[]{
                c.getCustomerId(),
                c.getFirstName(),
                middleName,
                c.getLastName(),
                fullName,
                c.getSex(),
                c.getPhoneNumber(),
                c.getEmail(),
                c.getAddress(),
                c.getLoyaltyPoint()
            });
        }
    }

