    public void filterProduct() {
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        String search = txtSearch.getText();
        String catId = "All";
        Object selected = cboCategory.getSelectedItem();
        if (selected instanceof Category) {
            catId = String.valueOf(((Category) selected).getCategoryId());
        }

        ArrayList<Product> list = ProductDao.filterProductByName(search, catId);
        Iterator<Product> itr = list.iterator();
        while (itr.hasNext()) {
            Product p = itr.next();
            dtm.addRow(new Object[]{p.getProductName(), p.getPrice()});
        }
    }

