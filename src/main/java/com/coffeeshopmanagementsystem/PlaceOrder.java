    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        // TODO add your handling code here:
        int index = jTable2.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this product?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TableModel model = jTable2.getModel();
            String totalStr = model.getValueAt(index, 3).toString();
            BigDecimal totalOfRow = new BigDecimal(totalStr);
            grandTotal = grandTotal.subtract(totalOfRow);
            lblGrandTotal.setText("Grand Total: VND " + grandTotal);
            cartList.remove(index);
            ((DefaultTableModel) jTable2.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_jTable2MouseClicked

