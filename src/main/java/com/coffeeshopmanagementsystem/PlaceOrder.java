    private void btnAddToCartActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddToCartActionPerformed
        // TODO add your handling code here:
        String name = txtProductName.getText();
        String priceStr = txtPrice.getText();
        String quantityStr = String.valueOf(jSpinner1.getValue());
        String totalStr = txtTotal.getText();
        if (name.equals("") || priceStr.equals("") || totalStr.equals("")) {
            JOptionPane.showMessageDialog(null, "Please select a product!");
            return;
        }
        int quantity = Integer.parseInt(quantityStr);
        if (quantity <= 0) {
            JOptionPane.showMessageDialog(null, "Quantity must be > 0");
            return;
        }
        Product product = ProductDao.getProductByName(name);
        if (product == null) {
            JOptionPane.showMessageDialog(null, "Product does not exist or has been discontinued!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        boolean isExist = false;
        int indexExist = -1;
        for (int i = 0; i < cartList.size(); i++) {
            if (cartList.get(i).getProductId() == product.getProductId()) {
                isExist = true;
                indexExist = i;
                break;
            }
        }
        if (isExist) {
            OrderDetail existingItem = cartList.get(indexExist);
            int newQuantity = existingItem.getQuantity() + quantity;
            existingItem.setQuantity(newQuantity);
            BigDecimal currentTotal = existingItem.getTotal();
            BigDecimal addTotal = new BigDecimal(totalStr);
            BigDecimal newTotal = currentTotal.add(addTotal);
            existingItem.setTotal(newTotal);
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.setValueAt(newQuantity, indexExist, 2);
            dtm.setValueAt(newTotal, indexExist, 3);
            grandTotal = grandTotal.add(addTotal);
        } else {
            OrderDetail orderDetail = new OrderDetail();
            orderDetail.setProductName(name);
            orderDetail.setPriceAtOrder(new BigDecimal(priceStr));
            orderDetail.setQuantity(quantity);
            orderDetail.setTotal(new BigDecimal(totalStr));
            orderDetail.setProductId(product.getProductId());
            cartList.add(orderDetail);
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
            dtm.addRow(new Object[]{name, priceStr, quantityStr, totalStr});
            grandTotal = grandTotal.add(new BigDecimal(totalStr));
        }
        lblGrandTotal.setText("Grand Total: " + grandTotal);
        clearProductFields();
    }//GEN-LAST:event_btnAddToCartActionPerformed

