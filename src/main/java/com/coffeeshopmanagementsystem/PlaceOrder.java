    private void txtPhoneNumberKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPhoneNumberKeyReleased
        // TODO add your handling code here:
        String phone = txtPhoneNumber.getText();

        if (phone.length() == 10) {
            Customer customer = CustomerDao.getCustomerByPhone(phone);
            if (customer != null) {
                txtFirstName.setText(customer.getFirstName());
                txtMiddleName.setText(customer.getMiddleName());
                txtLastName.setText(customer.getLastName());
                txtEmail.setText(customer.getEmail() == null ? "" : customer.getEmail());
                txtAddress.setText(customer.getAddress() == null ? "" : customer.getAddress());
                if (customer.getSex() != null && !customer.getSex().isEmpty()) {
                    cboSex.setSelectedItem(customer.getSex());
                } else {
                    cboSex.setSelectedIndex(0);
                }
                isCustomerLoaded = true;
            } else {
                if (isCustomerLoaded) {
                    txtFirstName.setText("");
                    txtMiddleName.setText("");
                    txtLastName.setText("");
                    txtEmail.setText("");
                    txtAddress.setText("");
                    cboSex.setSelectedIndex(0);
                    isCustomerLoaded = false;
                }
            }
        }
    }//GEN-LAST:event_txtPhoneNumberKeyReleased

    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
        // TODO add your handling code here:
        if (cartList.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Cart is empty!");
            return;
        }
        String firstName = txtFirstName.getText();
        String phone = txtPhoneNumber.getText();
        String email = txtEmail.getText();
        String description = txtDescription.getText();
        PaymentMethod selectedPayment = (PaymentMethod) cboPaymentMethod.getSelectedItem();
        String selectedSex = (String) cboSex.getSelectedItem();
        if (firstName.equals("") || phone.equals("") || email.equals("")
                || selectedPayment == null || selectedSex == null || selectedSex.equals("Not selected")) {
            JOptionPane.showMessageDialog(null, "Please fill out all fields!");
            return;
        }
        if (!phone.matches(phoneNumberPattern) || phone.length() != 10) {
            JOptionPane.showMessageDialog(null, "Invalid Phone Number!");
            return;
        }
        if (!email.matches(emailPattern)) {
            JOptionPane.showMessageDialog(null, "Invalid Email!");
            return;
        }
        try {
            int pointsEarned = grandTotal.divide(new BigDecimal(10000)).intValue();
            Customer existingCustomer = CustomerDao.getCustomerByPhone(phone);
            int customerIdToSave = 0;
            if (existingCustomer == null) {
                Customer newCust = new Customer();
                newCust.setFirstName(firstName);
                newCust.setMiddleName(txtMiddleName.getText());
                newCust.setLastName(txtLastName.getText());
                newCust.setSex(selectedSex);
                newCust.setPhoneNumber(phone);
                newCust.setEmail(email);
                newCust.setAddress(txtAddress.getText());
                newCust.setLoyaltyPoint(pointsEarned);
                CustomerDao.save(newCust);
                customerIdToSave = CustomerDao.getCustomerByPhone(phone).getCustomerId();
            } else {
                customerIdToSave = existingCustomer.getCustomerId();
                CustomerDao.updateLoyaltyPoints(phone, pointsEarned);
            }
            Orders order = new Orders();
            order.setTotalAmount(grandTotal);
            order.setCustomerId(customerIdToSave);
            order.setStaffId(userProcessId);
            order.setDescription(description);
            order.setPaymentId(selectedPayment.getPaymentId());
            String orderId = OrderDao.saveOrder(order, cartList);
            if (orderId != null) {
                String path = "D:\\Bill\\";
                File directory = new File(path);
                if (!directory.exists()) {
                    directory.mkdirs();
                }
                Document doc = new Document();

                try {
                    PdfWriter.getInstance(doc, new FileOutputStream(path + orderId + ".pdf"));
                    doc.open();
                    com.itextpdf.text.pdf.BaseFont bf = com.itextpdf.text.pdf.BaseFont.createFont("C:\\Windows\\Fonts\\arial.ttf", com.itextpdf.text.pdf.BaseFont.IDENTITY_H, com.itextpdf.text.pdf.BaseFont.EMBEDDED);
                    com.itextpdf.text.Font fontBold = new com.itextpdf.text.Font(bf, 18, com.itextpdf.text.Font.BOLD);
                    com.itextpdf.text.Font fontNormal = new com.itextpdf.text.Font(bf, 12, com.itextpdf.text.Font.NORMAL);
                    com.itextpdf.text.Font fontHeader = new com.itextpdf.text.Font(bf, 12, com.itextpdf.text.Font.BOLD);
                    Paragraph title = new Paragraph("PAYMENT INVOICE\n", fontBold);
                    title.setAlignment(com.itextpdf.text.Element.ALIGN_CENTER);
                    doc.add(title);
                    doc.add(new Paragraph("****************************************************************************************************************"));

                    String date = new SimpleDateFormat("dd/MM/yyyy HH:mm:ss").format(new java.util.Date());
                    Paragraph info = new Paragraph(
                            "\tOrder id: " + orderId
                            + "\nDate: " + date
                            + "\nTotal Paid: " + grandTotal + " VND"
                            + "\nPayment Method: " + selectedPayment.getMethodName(),
                            fontNormal
                    );
                    doc.add(info);
                    doc.add(new Paragraph("****************************************************************************************************************"));

                    Paragraph custInfo = new Paragraph(
                            "\tCustomer Name: " + txtLastName.getText() + " " + txtMiddleName.getText() + " " + firstName
                            + "\nPhone Number: " + phone,
                            fontNormal
                    );
                    doc.add(custInfo);

                    if (!description.equals("")) {
                        Paragraph descInfo = new Paragraph("\tDescription: " + description, fontNormal);
                        doc.add(descInfo);
                    }

                    doc.add(new Paragraph("\n"));

                    PdfPTable tb1 = new PdfPTable(4);
                    tb1.addCell(new Phrase("Name", fontHeader));
                    tb1.addCell(new Phrase("Price", fontHeader));
                    tb1.addCell(new Phrase("Quantity", fontHeader));
                    tb1.addCell(new Phrase("Total", fontHeader));

                    for (int i = 0; i < cartList.size(); i++) {
                        model.OrderDetail item = cartList.get(i);
                        tb1.addCell(new Phrase(item.getProductName(), fontNormal));
                        tb1.addCell(new Phrase(item.getPriceAtOrder().toString(), fontNormal));
                        tb1.addCell(new Phrase(String.valueOf(item.getQuantity()), fontNormal));
                        tb1.addCell(new Phrase(item.getTotal().toString(), fontNormal));
                    }
                    doc.add(tb1);

                    Paragraph thanks = new Paragraph("\nThank you, Please visit again!", fontNormal);
                    thanks.setAlignment(com.itextpdf.text.Element.ALIGN_CENTER);
                    doc.add(thanks);

                    doc.close();
                    common.OpenPdf.openById(orderId);

                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Error PDF: " + e.getMessage());
                }

                setVisible(false);
                new PlaceOrder(userProcessId).setVisible(true);

            } else {
                JOptionPane.showMessageDialog(null, "Error placing order!");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnGenerateBillActionPerformed

