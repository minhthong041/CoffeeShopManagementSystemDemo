    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        Staff oldStaff = StaffDao.getStaffById(staffIdToEdit);
        Staff newStaff = new Staff();
        newStaff.setStaffId(staffIdToEdit);
        newStaff.setFirstName(txtFirstName.getText().equals("") ? oldStaff.getFirstName() : txtFirstName.getText());
        newStaff.setMiddleName(txtMiddleName.getText().equals("") ? oldStaff.getMiddleName() : txtMiddleName.getText());
        newStaff.setLastName(txtLastName.getText().equals("") ? oldStaff.getLastName() : txtLastName.getText());
        newStaff.setEmail(txtEmail.getText().equals("") ? oldStaff.getEmail() : txtEmail.getText());
        newStaff.setPhoneNumber(txtPhoneNumber.getText().equals("") ? oldStaff.getPhoneNumber() : txtPhoneNumber.getText());
        newStaff.setAddress(txtAddress.getText().equals("") ? oldStaff.getAddress() : txtAddress.getText());
        String selectedSex = (String) cboSex.getSelectedItem();
        if (selectedSex == null || selectedSex.equals("Not selected")) {
            newStaff.setSex(oldStaff.getSex());
        } else {
            newStaff.setSex(selectedSex);
        }
        String selectedRoleName = (String) cboRole.getSelectedItem();
        if (selectedRoleName == null || selectedRoleName.equals("Not selected")) {
            newStaff.setRoleId(oldStaff.getRoleId()); // Giữ nguyên ID role cũ
        } else {
            String roleIdToSave = "";
            switch (selectedRoleName) {
                case "Manager":
                    roleIdToSave = "mgr";
                    break;
                case "Barista - Full time":
                    roleIdToSave = "baris_ft";
                    break;
                case "Barista - Part time":
                    roleIdToSave = "baris_pt";
                    break;
                case "Waiter/Waitress - Full time":
                    roleIdToSave = "wait_ft";
                    break;
                case "Waiter/Waitress - Part time":
                    roleIdToSave = "wait_pt";
                    break;
                default:
                    roleIdToSave = oldStaff.getRoleId(); // Nếu lỗi thì giữ cũ
            }
            newStaff.setRoleId(roleIdToSave);
        }
        if (datePickerDob.getDate() == null) {
            newStaff.setDateOfBirth(oldStaff.getDateOfBirth());
        } else {
            newStaff.setDateOfBirth(datePickerDob.getDate());
        }
        if (datePickerHiring.getDate() == null) {
            newStaff.setDateOfHiring(oldStaff.getDateOfHiring());
        } else {
            newStaff.setDateOfHiring(datePickerHiring.getDate());
        }
        String inputPass = txtPassword.getText().trim();

        if (inputPass.equals("")) {
            newStaff.setPassword(oldStaff.getPassword());
        } else {
            newStaff.setPassword(inputPass);
        }
        newStaff.setStatus(oldStaff.getStatus());
        StaffDao.update(newStaff);
        setVisible(false);
    }//GEN-LAST:event_btnUpdateActionPerformed

