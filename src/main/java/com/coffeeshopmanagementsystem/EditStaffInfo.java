    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        Staff staff = StaffDao.getStaffById(staffIdToEdit);
        if (staff != null) {
            txtFirstName.setText(staff.getFirstName());
            txtMiddleName.setText(staff.getMiddleName());
            txtLastName.setText(staff.getLastName());
            txtPhoneNumber.setText(staff.getPhoneNumber());
            txtEmail.setText(staff.getEmail());
            txtAddress.setText(staff.getAddress());
            txtPassword.setText(staff.getPassword());
            String sex = staff.getSex();
            if (sex != null && !sex.isEmpty()) {
                cboSex.setSelectedItem(sex);
            } else {
                cboSex.setSelectedIndex(0);
            }
            String roleId = staff.getRoleId();
            if (roleId != null) {
                switch (roleId) {
                    case "mgr":
                        cboRole.setSelectedItem("Manager");
                        break;
                    case "baris_ft":
                        cboRole.setSelectedItem("Barista - Full time");
                        break;
                    case "baris_pt":
                        cboRole.setSelectedItem("Barista - Part time");
                        break;
                    case "wait_ft":
                        cboRole.setSelectedItem("Waiter/Waitress - Full time");
                        break;
                    case "wait_pt":
                        cboRole.setSelectedItem("Waiter/Waitress - Part time");
                        break;
                    default:
                        cboRole.setSelectedIndex(0);
                }
            }
            if (staff.getDateOfBirth() != null) {
                datePickerDob.setDate(staff.getDateOfBirth());
            }
            if (staff.getDateOfHiring() != null) {
                datePickerHiring.setDate(staff.getDateOfHiring());
            }
        }
    }//GEN-LAST:event_formComponentShown

